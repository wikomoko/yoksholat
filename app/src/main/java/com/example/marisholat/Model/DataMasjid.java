package com.example.marisholat.Model;

public class DataMasjid {
    private String lokasi;
    private String provinsi;
    private String nmasjid;
    private String gmasjid;
    private String ket;

    public String getLokasi() {
        return lokasi;
    }

    public void setLokasi(String lokasi) {
        this.lokasi = lokasi;
    }

    public String getProvinsi() {
        return provinsi;
    }

    public void setProvinsi(String provinsi) {
        this.provinsi = provinsi;
    }

    public String getNmasjid() {
        return nmasjid;
    }

    public void setNmasjid(String nmasjid) {
        this.nmasjid = nmasjid;
    }

    public String getGmasjid() {
        return gmasjid;
    }

    public void setGmasjid(String gmasjid) {
        this.gmasjid = gmasjid;
    }

    public String getKet() {
        return ket;
    }

    public void setKet(String ket) {
        this.ket = ket;
    }
}
